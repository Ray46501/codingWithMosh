is_hot = True # Sets is_hot to boolean value True
is_cold = False


if is_hot: # Checks value of if is_hot is true or false
    print("It's a hot day")
    print("Drink enough water")
elif is_cold: # Checks value of if is_cold is true or false
    print("It's a cold day")
    print("Wear warm clothes")    
else: # If none of the above are true print its a lovely day
    print("It's a lovely day!")    

print("Enjoy your day!") # Prints regardless of of the value of is_hot or is_cold as it is not indented into the if statement