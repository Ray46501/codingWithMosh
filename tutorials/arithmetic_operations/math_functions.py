import math # Imports math module gives acces to function not available in normal python

x = 2.9

print(round(x)) # Will round x -> 3
print(abs(-2.9)) # Takes absolute value of -2.9 -> 2.9
print(math.ceil(x)) # Performs ceiling function on x -> 3
print(math.floor(x)) # Performs floor function on x -> 2