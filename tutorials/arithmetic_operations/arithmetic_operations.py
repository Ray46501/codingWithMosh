print(10 + 3) # 10 plus 3
print(10 - 3) # 10 minus 3
print(10 * 3) # 10 times 3
print(10 / 3) # 10 Divided by 3
print(10 // 3) # 10 divided by 3 rounded
print(10 % 3) # Remainder of 10 divided by 3
print(10 ** 3) # 10 to the power of 3

x = 10
x = x + 3 # Adds 3 to x's current value
x += 3 # Shorter way to increment value
x -=3 # Shorter way to decrement
